#include "libft.h"

void	ft_num(int d, int *len)
{
	if (d == -2147483648)
	{
		ft_s("-2147483648", len);
		return ;
	}
	if (d < 0)
	{
		write (1, "-", 1);
		d = -d;
		(*len)++;
	}
	if (d > 9)
		ft_num(d / 10, len);
	ft_c(d % 10 + '0', len);
}
